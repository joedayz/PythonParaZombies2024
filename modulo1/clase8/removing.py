d = [1, 2, 3, 4, 5, 6]
print('List d:',d)

print('\nRemoviendo el valor del indice 1 de la lista d usando pop()')
d.pop(1)
print('List d:',d)

print('\nRemoviendo el valor 3 de la lista d usando remove()')
d.remove(3)
print('List d:',d)

print('\nRemoviendo todos lo valores de la lista d usando clear()')
d.clear()
print('List d:',d)